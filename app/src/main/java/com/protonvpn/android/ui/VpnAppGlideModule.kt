package com.protonvpn.android.ui

import android.content.Context
import android.content.pm.ApplicationInfo
import android.graphics.drawable.Drawable
import com.bumptech.glide.Glide
import com.bumptech.glide.Registry
import com.bumptech.glide.annotation.GlideModule
import com.bumptech.glide.module.AppGlideModule
import com.protonvpn.android.components.AppIconModelLoader

@GlideModule
class VpnAppGlideModule : AppGlideModule() {
    override fun registerComponents(context: Context, glide: Glide, registry: Registry) {
        super.registerComponents(context, glide, registry)
        val appIconModelLoaderFactory = AppIconModelLoader.Factory(context.packageManager)
        registry.append(ApplicationInfo::class.java, Drawable::class.java, appIconModelLoaderFactory)
    }
}
